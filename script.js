document.addEventListener('DOMContentLoaded', () => {
    // --- Theme Toggling ---
    const themeToggleBtn = document.getElementById('theme-toggle');
    const themeIcon = themeToggleBtn.querySelector('i');
    
    // Check local storage for theme
    const currentTheme = localStorage.getItem('theme') || 'light';
    if(currentTheme === 'dark') {
        document.documentElement.setAttribute('data-theme', 'dark');
        themeIcon.classList.replace('fa-moon', 'fa-sun');
    }

    themeToggleBtn.addEventListener('click', () => {
        let theme = document.documentElement.getAttribute('data-theme');
        if (theme === 'dark') {
            document.documentElement.setAttribute('data-theme', 'light');
            localStorage.setItem('theme', 'light');
            themeIcon.classList.replace('fa-sun', 'fa-moon');
        } else {
            document.documentElement.setAttribute('data-theme', 'dark');
            localStorage.setItem('theme', 'dark');
            themeIcon.classList.replace('fa-moon', 'fa-sun');
        }
    });

    // --- Language Toggling ---
    const langToggleBtn = document.getElementById('lang-toggle');
    const langIndicator = document.getElementById('lang-indicator');
    
    // Check local storage for language
    let currentLang = localStorage.getItem('lang') || 'en';
    if(currentLang === 'hi') {
        langIndicator.textContent = "EN";
        applyTranslations('hi');
    }

    langToggleBtn.addEventListener('click', () => {
        if (currentLang === 'en') {
            currentLang = 'hi';
            langIndicator.textContent = "EN";
        } else {
            currentLang = 'en';
            langIndicator.textContent = "HI";
        }
        localStorage.setItem('lang', currentLang);
        applyTranslations(currentLang);
    });

    function applyTranslations(lang) {
        document.querySelectorAll("[data-i18n]").forEach(el => {
            const key = el.getAttribute("data-i18n");
            if (translations[lang][key]) {
                el.innerText = translations[lang][key];
            }
        });

        document.querySelectorAll("[data-i18n-placeholder]").forEach(el => {
            const key = el.getAttribute("data-i18n-placeholder");
            if (translations[lang][key]) {
                el.setAttribute("placeholder", translations[lang][key]);
            }
        });
    }


    // --- Navbar scroll effect ---
    const navbar = document.querySelector('.navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // --- Mobile menu toggle ---
    const hamburger = document.querySelector('.hamburger');
    const navLinks = document.querySelector('.nav-links');
    const links = document.querySelectorAll('.nav-links li a');

    hamburger.addEventListener('click', () => {
        navLinks.classList.toggle('active');
        const icon = hamburger.querySelector('i');
        if (navLinks.classList.contains('active')) {
            icon.classList.remove('fa-bars');
            icon.classList.add('fa-times');
        } else {
            icon.classList.remove('fa-times');
            icon.classList.add('fa-bars');
        }
    });

    links.forEach(link => {
        link.addEventListener('click', () => {
            navLinks.classList.remove('active');
            const icon = hamburger.querySelector('i');
            icon.classList.remove('fa-times');
            icon.classList.add('fa-bars');
        });
    });

    // --- Intersection Observer for scroll animations ---
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.15
    };

    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('active');
                observer.unobserve(entry.target); 
            }
        });
    }, observerOptions);

    const animatedElements = document.querySelectorAll('.reveal-text, .fade-in-up, .slide-in-left, .slide-in-right, .slide-up');
    animatedElements.forEach(el => observer.observe(el));

    // --- Staggered animation for service cards ---
    const staggerObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                const cards = entry.target.querySelectorAll('.stagger-in');
                cards.forEach((card, index) => {
                    setTimeout(() => {
                        card.classList.add('active');
                    }, index * 100);
                });
                staggerObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.1 });

    const servicesGrid = document.querySelector('.services-grid');
    if (servicesGrid) {
        staggerObserver.observe(servicesGrid);
    }

    // --- Date Validation (No Past Dates) ---
    const dateInput = document.querySelector('input[type="date"]');
    if (dateInput) {
        const today = new Date().toISOString().split('T')[0];
        dateInput.setAttribute('min', today);
    }

    // --- WhatsApp Form Redirect ---
    const contactForms = document.querySelectorAll('.contact-form');
    contactForms.forEach(form => {
        form.addEventListener('submit', function(e) {
            e.preventDefault();
            
            const serviceSelect = this.querySelector('select');
            const service = serviceSelect.options[serviceSelect.selectedIndex].text;
            
            const inputs = this.querySelectorAll('input');
            const name = inputs[0].value;
            const phone = inputs[1].value;
            const date = inputs[2].value;
            const time = inputs[3].value;
            const location = inputs[4].value;
            const message = this.querySelector('textarea').value;
            
            const whatsappText = `Hari Om Pandit Ji,\nI would like to book a sacred service:\n\n*Service:* ${service}\n*Name:* ${name}\n*Phone:* ${phone}\n*Date:* ${date}\n*Time:* ${time}\n*Location:* ${location}\n*Message:* ${message ? message : 'N/A'}`;
            
            const targetPhone = "918349615622";
            const wURL = `https://wa.me/${targetPhone}?text=${encodeURIComponent(whatsappText)}`;
            window.open(wURL, '_blank');
        });
    });

    // --- Help Form Redirect ---
    const helpForm = document.querySelector('.help-form');
    if (helpForm) {
        helpForm.addEventListener('submit', function(e) {
            e.preventDefault();
            const message = this.querySelector('textarea').value;
            const whatsappText = `Hari Om Pandit Ji,\nI need some help:\n\n*Message:* ${message}`;
            const targetPhone = "918349615622";
            const wURL = `https://wa.me/${targetPhone}?text=${encodeURIComponent(whatsappText)}`;
            window.open(wURL, '_blank');
            this.querySelector('textarea').value = '';
        });
    }

    // --- Modal Logic ---
    const modal = document.getElementById('booking-modal');
    const btnBookModal = document.getElementById('btn-book-modal');
    const closeBtn = document.querySelector('.close-modal');

    if (btnBookModal && modal) {
        btnBookModal.addEventListener('click', (e) => {
            e.preventDefault();
            modal.style.display = 'flex';
            // Slight delay so the transition triggers
            setTimeout(() => modal.classList.add('show'), 10);
            
            // Re-apply translations for modal specifically if needed, 
            // but it pulls from the global attribute map anyway.
            const currentLang = localStorage.getItem('lang') || 'en';
            if (currentLang === 'hi') {
                document.querySelectorAll("#booking-modal [data-i18n]").forEach(el => {
                    const key = el.getAttribute("data-i18n");
                    if (translations[currentLang][key]) el.innerText = translations[currentLang][key];
                });
                document.querySelectorAll("#booking-modal [data-i18n-placeholder]").forEach(el => {
                    const key = el.getAttribute("data-i18n-placeholder");
                    if (translations[currentLang][key]) el.setAttribute("placeholder", translations[currentLang][key]);
                });
            }
        });

        closeBtn.addEventListener('click', () => {
            modal.classList.remove('show');
            setTimeout(() => modal.style.display = 'none', 300);
        });

        window.addEventListener('click', (e) => {
            if (e.target == modal) {
                modal.classList.remove('show');
                setTimeout(() => modal.style.display = 'none', 300);
            }
        });
    }

    // --- Image Modal Logic (Lightbox) ---
    const imageModal = document.getElementById('image-modal');
    const expandedImg = document.getElementById('expanded-image');
    const closeImgModal = document.querySelector('.close-image-modal');

    if (imageModal && expandedImg && closeImgModal) {
        document.querySelectorAll('.gallery-item img').forEach(img => {
            img.style.cursor = 'pointer';
            img.addEventListener('click', () => {
                expandedImg.src = img.src;
                imageModal.style.display = 'flex';
                setTimeout(() => imageModal.classList.add('show'), 10);
            });
        });

        closeImgModal.addEventListener('click', () => {
            imageModal.classList.remove('show');
            setTimeout(() => imageModal.style.display = 'none', 300);
        });

        imageModal.addEventListener('click', (e) => {
            if (e.target !== expandedImg) {
                imageModal.classList.remove('show');
                setTimeout(() => imageModal.style.display = 'none', 300);
            }
        });
    }
});
